#include <stdio.h>

int	check_args(int argc, char *argv[])
{
	int	i;

	i = -1;
	if (argc != 2)
	{
		return (0);
	}
	while (i++ < 30)
	{
		if (i % 2 == 0 && (argv[1][i] < '1' || argv[1][i] > '4'))
			return (0);
		if (i % 2 == 1 && argv[1][i] != 32)
			return (0);
	}
	if (i != 31)
		return (0);
	i = -1;
	while (i++ < 22)
	{
		if (i == 8)
			i = 15;
		if (i % 2 == 0 && argv[1][i] - 48 + argv[1][i + 8] - 48 > 5)
			return (0);
	}
	return (1);
}
